void main() {
  //number adalah tipe data angkat yang terdiri dua jenis tipe yaitu int dan double
  //int untuk bilangan bulat
  //double untuk bilang desimal

  int number_1 = 10;
  double number_2 = 10.5;

  print(number_1);
  print(number_2);

  //jika kita ingin menggunakan tipe data number yang bisa int double kita bisa menggunakan num

  num number = 100;
  print(number);
  number = 10.45;

  print(number);
}
